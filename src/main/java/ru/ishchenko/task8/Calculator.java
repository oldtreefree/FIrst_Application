package ru.ishchenko.task8;

public class Calculator {

    public static double add(double i, double i2) {
        return i + i2;
    }

    public static double dec(double i, double i2) {
        return i - i2;
    }

    public static double mul(double i, double i2) {
        return i * i2;
    }

    public static double sub(double i, double i2) {
        return i / i2;
    }

    public static double percent(double i, double i2) {
        return i * i2 / 100;
    }

}
